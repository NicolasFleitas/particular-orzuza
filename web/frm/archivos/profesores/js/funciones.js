function validarFormulario(){
    var valor=true;
    if ($("#nombre_profesor").val().trim()===""){
        valor=false;
        $("#mensajes").html("Ingrese un nombre de profesor por favor");
        $("#nombre_profesor").focus();    
    }  else if ($("#apellido_profesor").val().trim()===""){
        valor=false;
        $("#mensajes").html("Ingrese un apellido de profesor por favor");
        $("#apellido_profesor").focus();    
    } else if ($("#nroci_profesor").val().trim()===""){
        valor=false;
        $("#mensajes").html("Ingrese el numero de Cedula del profesor por favor");
        $("#nroci_profesor").focus();   
    }  else if ($("#telefono_profesor").val().trim()===""){
        valor=false;
        $("#mensajes").html("Ingrese el numero de Telefono del profesor por favor");
        $("#telefono_profesor").focus();   
    } 
    
    
    return valor;
}
function agregarProfesor() {
    var datosFormulario = $("#formPrograma").serialize();
    $.ajax({
        type: 'POST',
        url: 'jsp/agregar.jsp',
        data: datosFormulario,
        dataType: 'json',
        beforeSend: function (objeto) {
            $("#mensajes").html("Enviando datos al Servidor ...");
        },
        success: function (json) {
            $("#mensajes").html(json.mensaje);
            limpiarFormulario();
            $("#id_profesor").focus();
            $("#id_profesor").select();
        },
        error: function (e) {
            $("#mensajes").html("No se pudo modificar los datos.");
        },
        complete: function (objeto, exito, error) {
            $("#id_profesor").focus();
        }
    });
}

